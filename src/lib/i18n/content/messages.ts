export function getContent(locale: UiLocale): ContentCatalog {
  return CONTENT[locale];
}

export function htmlLang(locale: UiLocale): string {
  if (locale === "zh-CN") return "zh-Hans";
  return locale;
}

export function intlLocale(locale: UiLocale): string {
  const map: Record<UiLocale, string> = {
    ja: "ja-JP",
    en: "en-US",
    ko: "ko-KR",
    "zh-CN": "zh-CN",
  };
  return map[locale];
}
